import mongoose from "mongoose"
// Product Schema
const ProductSchema = mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    slug: {
        type: String
    },
    desc: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    image: {
        type: String
    }

});

const Products = mongoose.model("Products", ProductSchema);
module.exports = Products;
